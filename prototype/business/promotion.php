<?php

class Promotion{
    private $id;
    private $name;

    public function getId(){
        return $this->id;
    }

    public function setId(){
        $this->id = $this->id;
    }

    public function getName(){
        return $this->name;
    }

    public function setName(){
        $this->name = $this->name;
    }
}